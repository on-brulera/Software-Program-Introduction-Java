    private void CbFigurasGeometricasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbFigurasGeometricasActionPerformed
        this.opcion = this.CbFigurasGeometricas.getSelectedIndex();
        if (opcion == 0) {
            this.acederMetodos = 1;
            this.OcultarAtributos(true);
            this.RestaurarValores();
            this.AtributosRecta();
        } else {
            if (this.opcion == 1) {
                this.acederMetodos = 1;
                this.OcultarAtributos(true);
                this.RestaurarValores();
                this.AtributosTriangulo();
            } else {
                if (this.opcion == 2) {
                    this.acederMetodos = 1;
                    this.OcultarAtributos(true);
                    this.RestaurarValores();
                    this.AtributosCuadrado();
                } else {
                    if (this.opcion == 3) {
                        this.acederMetodos = 1;
                        this.OcultarAtributos(true);
                        this.RestaurarValores();
                        this.AtributosRectangulo();
                    } else {
                        if (this.opcion == 4) {
                            this.acederMetodos = 1;
                            this.OcultarAtributos(true);
                            this.RestaurarValores();
                            this.AtributosCircunferencia();
                        } else {
                            this.acederMetodos = 1;
                            this.OcultarAtributos(true);
                            this.RestaurarValores();
                            this.AtributosElipse();
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_CbFigurasGeometricasActionPerformed

