    public void CambiarPosicion() {
        if (this.opcion == 0) {
            if (this.mover == 0) {
                this.recta.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                this.TxtPosicion.setText(this.recta.puntoInicio.Coordenada());
                this.txtNuevaPosicion.setText(this.recta.puntoInicio.Coordenada());
            } else {
                if (this.mover == 1) {
                    this.recta.puntoInicio.setX(Integer.parseInt(this.txtDx.getText()));
                    this.recta.puntoInicio.setY(Integer.parseInt(this.TxtDy.getText()));
                    this.TxtPosicion.setText(this.recta.puntoInicio.Coordenada());
                    this.txtNuevaPosicion.setText(this.recta.puntoInicio.Coordenada());
                } else {
                    this.TxtPosicion.setText(this.recta.puntoInicio.Coordenada());
                }
            }
        } else {
            if (this.opcion == 1) {
                if (this.mover == 0) {
                    this.triangulo.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                    this.TxtPosicion.setText(this.triangulo.verticeA.Coordenada());
                    this.txtNuevaPosicion.setText(this.triangulo.verticeA.Coordenada());
                } else {
                    if (this.mover == 1) {
                        this.triangulo.verticeA.setX(this.triangulo.verticeA.getX() + Integer.parseInt(this.txtDx.getText()));
                        this.triangulo.verticeA.setY(this.triangulo.verticeA.getY() + Integer.parseInt(this.TxtDy.getText()));
                        this.TxtPosicion.setText(this.triangulo.verticeA.Coordenada());
                        this.txtNuevaPosicion.setText(this.triangulo.verticeA.Coordenada());
                    } else {
                        this.TxtPosicion.setText(this.triangulo.verticeA.Coordenada());
                    }
                }
            } else {
                if (this.opcion == 2) {
                    if (this.mover == 0) {
                        this.cuadrado.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                        this.TxtPosicion.setText(this.cuadrado.puntoOrigen.Coordenada());
                        this.txtNuevaPosicion.setText(this.cuadrado.puntoOrigen.Coordenada());
                    } else {
                        if (this.mover == 1) {
                            this.cuadrado.puntoOrigen.setX(Integer.parseInt(this.txtDx.getText()));
                            this.cuadrado.puntoOrigen.setY(Integer.parseInt(this.TxtDy.getText()));
                            this.TxtPosicion.setText(this.cuadrado.puntoOrigen.Coordenada());
                            this.txtNuevaPosicion.setText(this.cuadrado.puntoOrigen.Coordenada());
                        } else {
                            this.TxtPosicion.setText(this.cuadrado.puntoOrigen.Coordenada());
                        }
                    }
                } else {
                    if (this.opcion == 3) {
                        if (this.mover == 0) {
                            this.rectangulo.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                            this.TxtPosicion.setText(this.rectangulo.puntoOrigen.Coordenada());
                            this.txtNuevaPosicion.setText(this.rectangulo.puntoOrigen.Coordenada());
                        } else {
                            if (this.mover == 1) {
                                this.rectangulo.puntoOrigen.setX(this.rectangulo.puntoOrigen.getX() + Integer.parseInt(this.txtDx.getText()));
                                this.rectangulo.puntoOrigen.setY(this.rectangulo.puntoOrigen.getY() + Integer.parseInt(this.TxtDy.getText()));
                                this.TxtPosicion.setText(this.rectangulo.puntoOrigen.Coordenada());
                                this.txtNuevaPosicion.setText(this.rectangulo.puntoOrigen.Coordenada());
                            } else {
                                this.TxtPosicion.setText(this.rectangulo.puntoOrigen.Coordenada());
                            }
                        }
                    } else {
                        if (this.opcion == 4) {
                            if (this.mover == 0) {
                                this.circunferencia.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                                this.TxtPosicion.setText(this.circunferencia.centro.Coordenada());
                                this.txtNuevaPosicion.setText(this.circunferencia.centro.Coordenada());
                            } else {
                                if (this.mover == 1) {
                                    this.circunferencia.centro.setX(Integer.parseInt(this.txtDx.getText()));
                                    this.circunferencia.centro.setY(Integer.parseInt(this.TxtDy.getText()));
                                    this.TxtPosicion.setText(this.circunferencia.centro.Coordenada());
                                    this.txtNuevaPosicion.setText(this.circunferencia.centro.Coordenada());
                                } else {
                                    this.TxtPosicion.setText(this.circunferencia.centro.Coordenada());
                                }
                            }
                        } else {
                            if (this.mover == 0) {
                                this.elipse.Mover(Integer.parseInt(this.txtDx.getText()), Integer.parseInt(this.TxtDy.getText()));
                                this.TxtPosicion.setText(this.elipse.centro.Coordenada());
                                this.txtNuevaPosicion.setText(this.elipse.centro.Coordenada());
                            } else {
                                if (this.mover == 1) {
                                    this.elipse.centro.setX(Integer.parseInt(this.txtDx.getText()));
                                    this.elipse.centro.setY(Integer.parseInt(this.TxtDy.getText()));
                                    this.TxtPosicion.setText(this.elipse.centro.Coordenada());
                                    this.txtNuevaPosicion.setText(this.elipse.centro.Coordenada());
                                } else {
                                    this.TxtPosicion.setText(this.elipse.centro.Coordenada());
                                }
                            }
                        }
                    }
                }
            }
        }
    }


