    private void BtnCompararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCompararActionPerformed
        if (this.f2 == null) {
            this.f2 = new Fecha(Integer.parseInt(this.TxtAñoComparar.getText()), Integer.parseInt(this.TxtMesComparar.getText()), Integer.parseInt(this.TxtDiaComparar.getText()));
            this.CompararFechas(this.f1.Comparar(f2));
        } else {
            this.f2.setAño(Integer.parseInt(this.TxtAñoComparar.getText()));
            this.f2.setMes(Integer.parseInt(this.TxtMesComparar.getText()));
            this.f2.setMes(Integer.parseInt(this.TxtDiaComparar.getText()));
            this.CompararFechas(this.f1.Comparar(f2));
        }
        this.MostrarEscribirFechaComparar(false);
    }//GEN-LAST:event_BtnCompararActionPerformed

