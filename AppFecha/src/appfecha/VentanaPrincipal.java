    private void CbEscogerFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbEscogerFechaActionPerformed
        this.ActivarBotones(false);
        this.ReiniciarValores();
        if (this.CbEscogerFecha.getSelectedIndex() == 0 && f1 == null) {
            this.f1 = new Fecha();
            this.TxtFechaSeleccionada.setText(f1.Imprimir());
            this.ActivarBotones(true);
        } else {
            if (this.f1 != null && this.CbEscogerFecha.getSelectedIndex() == 0) {
                this.f1.setAño(fecha.get(Calendar.YEAR));
                this.f1.setMes(fecha.get(Calendar.MONTH) + 1);
                this.f1.setDia(fecha.get(Calendar.DAY_OF_MONTH));
                this.TxtFechaSeleccionada.setText(f1.Imprimir());
                this.ActivarBotones(true);
            } else {
                this.MostrarEscribirFecha(true);
            }
        }
        this.TxtFechaSeleccionada.setText(this.f1.Imprimir());
        this.TxtSumarUnDia.setText(this.f1.Imprimir());
        this.TxtSumarVariosDias.setText(this.f1.Imprimir());
        this.TxtRestarUnDia.setText(this.f1.Imprimir());
        this.TxtRestarVariosDias.setText(this.f1.Imprimir());
    }//GEN-LAST:event_CbEscogerFechaActionPerformed

