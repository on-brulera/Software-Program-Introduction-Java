    public void CompararFechas(int respuesta) {
        respuesta *= -1;
        if (respuesta == 1) {
            this.TxtComparar.setText("La fecha es mayor");
        } else {
            if (respuesta == -1) {
                this.TxtComparar.setText("La fecha es menor");
            } else {
                this.TxtComparar.setText("Fechas iguales");
            }
        }
    }

