    private void FechaOpcionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FechaOpcionesActionPerformed
        this.ReiniciarValores();
        if (this.FechaOpciones.getSelectedIndex() == 0 && f1 == null) {
            this.f1 = new Fecha();
            this.FechaSeleccionada.setText(f1.Imprimir());
            this.ActivarBotones(true);
        } else {
            if (this.f1 != null && this.FechaOpciones.getSelectedIndex() == 0) {
                this.f1.setAño(fecha.get(Calendar.YEAR));
                this.f1.setMes(fecha.get(Calendar.MONTH) + 1);
                this.f1.setDia(fecha.get(Calendar.DAY_OF_MONTH));
                this.FechaSeleccionada.setText(f1.Imprimir());
                this.ActivarBotones(true);
            } else {
                this.EscribirFechaOpcion2(false);
                this.BtnAceptarFecha.setEnabled(true);
            }
        }        
    }//GEN-LAST:event_FechaOpcionesActionPerformed

