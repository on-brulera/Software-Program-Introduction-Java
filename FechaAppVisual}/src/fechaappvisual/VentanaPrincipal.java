    private void CuadroOpcionesCompararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CuadroOpcionesCompararActionPerformed
        if (this.FechaSeleccionada.getText().length() > 0) {
            if (this.CuadroOpcionesComparar.getSelectedIndex() == 0) {
                if (this.f2 == null) {
                    this.f2 = new Fecha();
                    this.CompararFechas();
                } else {
                    this.CompararFechas();
                }
            } else {
                if (this.CuadroOpcionesComparar.getSelectedIndex() == 1) {
                    this.MostrarFechaComparar(true);
                } else {
                    if (this.f2 == null) {
                        this.f2 = new Fecha(2000, 1, 1);
                        this.CompararFechas();
                    } else {
                        this.CompararFechas();
                    }
                }
            }
        }
    }//GEN-LAST:event_CuadroOpcionesCompararActionPerformed

