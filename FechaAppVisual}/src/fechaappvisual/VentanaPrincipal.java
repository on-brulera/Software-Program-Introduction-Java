    private void BtnFechaCompararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnFechaCompararActionPerformed
        int año = Integer.parseInt(this.AñoComparar.getText());
        int mes = Integer.parseInt(this.MesComparar.getText());
        int dia = Integer.parseInt(this.DiasComparar.getText());
        if (this.f2 == null) {
            this.f2 = new Fecha(año, mes, dia);
            this.CompararFechas();
        } else {
            this.f2.setAño(año);
            this.f2.setMes(mes);
            this.f2.setDia(dia);
            this.CompararFechas();
        }
        this.MostrarFechaComparar(false);
    }//GEN-LAST:event_BtnFechaCompararActionPerformed

