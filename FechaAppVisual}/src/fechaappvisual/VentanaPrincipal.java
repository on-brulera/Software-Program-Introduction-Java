    public void CompararFechas() {
        this.f2 = new Fecha();
        if (this.f2.Comparar(this.f1.getAño(), this.f1.getMes(), this.f1.getDia()) == 1) {
            this.ResultadoComparar.setText("La fecha es mayor que la del sistema");
        } else {
            if (this.f2.Comparar(this.f1.getAño(), this.f1.getMes(), this.f1.getDia()) == -1) {
                this.ResultadoComparar.setText("La fecha es menor que la del sistema");
            } else {
                this.ResultadoComparar.setText("La fecha es igual que la del sistema");
            }
        }
    }
    
    public void EscribirFechaOpcion2(boolean ocultar) {
        if (ocultar == true) {
            this.LbAño.setVisible(false);
            this.LbMes.setVisible(false);
            this.LbDia.setVisible(false);
            this.EscribirAño.setVisible(false);
            this.EscribirMes.setVisible(false);
            this.EscribirDia.setVisible(false);
            this.BtnAceptarFecha.setVisible(false);
        } else {
            this.LbAño.setVisible(true);
            this.LbMes.setVisible(true);
            this.LbDia.setVisible(true);
            this.EscribirAño.setVisible(true);
            this.EscribirMes.setVisible(true);
            this.EscribirDia.setVisible(true);
            this.BtnAceptarFecha.setVisible(true);
        }
    }
    
