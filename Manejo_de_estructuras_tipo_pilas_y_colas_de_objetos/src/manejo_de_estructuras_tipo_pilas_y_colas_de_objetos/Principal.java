    private void btn_desapilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_desapilarActionPerformed
        // TODO add your handling code here:

        if (pila_principal.Vacia()) {
             JOptionPane.showMessageDialog(null, "Pila  vacia");
        }
        else{Object cima = pila_principal.Peek();
        pila_principal.Desapilar();

        if (cima instanceof Figura_geometrica) {
            pila_figuras.apilar(cima);
            lbl_figuras.setText(pila_figuras.Peek().toString());
            txtArea_Principal.setText(pila_principal.toString());
        } else {
            if (cima instanceof Coordenadas_polares || cima instanceof Coordenadas_rectangulares) {
                pila_Coordenadas.apilar(cima);
                lbl_coor.setText(pila_Coordenadas.Peek().toString());
                txtArea_Principal.setText(pila_principal.toString());
            } else {
                if (cima instanceof Cuenta) {
                    pila_cuenta.apilar(cima);
                    txtarea_cuenta.setText(pila_cuenta.Peek().toString());
                    txtArea_Principal.setText(pila_principal.toString());
                } else {
                    if (cima instanceof Fecha) {
                        pila_fecha.apilar(cima);
                        lbl_fecha.setText(pila_fecha.Peek().toString());
                        txtArea_Principal.setText(pila_principal.toString());
                    } else {
                        if (cima instanceof Fraccionario) {
                            pila_fraccionario.apilar(cima);
                            lbl_fraccion.setText(pila_fraccionario.Peek().toString());
                            txtArea_Principal.setText(pila_principal.toString());
                        } else {

                            pila_numerosComplejos.apilar(cima);
                            lbl_numerosComplejos.setText(pila_numerosComplejos.Peek().toString());
                            txtArea_Principal.setText(pila_principal.toString());
                        }
                    }
                }
            }
        }}
        
        

    }//GEN-LAST:event_btn_desapilarActionPerformed

