    private void btn_ingresarFigurasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarFigurasActionPerformed
        // TODO add your handling code here:

        if (pila_figuras == null) {
            JOptionPane.showMessageDialog(null, "Pila no creada");
        } else {
            if (pila_figuras.Vacia()) {
                JOptionPane.showMessageDialog(null, "Pila  vacia");
            } else {
                Object aux = pila_figuras.Peek();
                pila_figuras.Desapilar();
                if (pila_figuras.Vacia()) {
                    lbl_figuras.setText(".........");
                } else {
                    lbl_figuras.setText(pila_figuras.Peek().toString());
                }

                //txtArea_Principal.append("\n" + aux.toString() + "\n");

                pila_principal.apilar(aux);
                txtArea_Principal.setText(pila_principal.toString());
            }
        }


    }//GEN-LAST:event_btn_ingresarFigurasActionPerformed

