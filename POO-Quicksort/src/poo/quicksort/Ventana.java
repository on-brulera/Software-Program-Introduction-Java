    private void btn_ordenarQuickSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ordenarQuickSortActionPerformed
        // TODO add your handling code here:
        if (this.arreglo == null) {
            JOptionPane.showMessageDialog(null, "Revisa el arreglo");
        } else {
            long inicio = System.currentTimeMillis();
            this.arreglo.QuickSort(this.arreglo.lista, 0, this.arreglo.lista.length - 1);
            long fin = System.currentTimeMillis();
            double tiempo = (double) ((fin - inicio) / 1000);
            this.txt_segundosQuickSort.setText(tiempo + "");
            this.arreglo.Imprimir2(this.arreglo.lista);
            this.txt_milisegundos1.setText((fin - inicio) + "");
        }
    }//GEN-LAST:event_btn_ordenarQuickSortActionPerformed

