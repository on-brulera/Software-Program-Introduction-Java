    public void llenarArreglo() {
        if (this.indice < this.arreglo.lista.length) {
            this.lb_llenarArreglo.setText("lista, posición [" + (indice + 1) + "]:");
            this.arreglo.CambiarValores(this.indice, Integer.parseInt(this.txt_llenarArreglo.getText()));
            this.txt_llenarArreglo.setText("0");
            this.indice++;
        }
    }

