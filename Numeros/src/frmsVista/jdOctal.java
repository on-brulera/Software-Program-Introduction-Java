    private boolean ValidarOctal() {

        String nOctal = txtNumero.getText();
        boolean n = false;

        String[] octal = nOctal.split(",");

        for (int i = 0; i < octal[0].length(); i++) {
            if (Integer.parseInt(String.valueOf(octal[0].charAt(i))) >= 0 && Integer.parseInt(String.valueOf(octal[0].charAt(i))) <= 8) {
                n = true;
            } else {
                n = false;
            }
        }

        if (octal.length > 1) {
            for (int i = 0; i < octal[1].length(); i++) {
                if (Integer.parseInt(String.valueOf(octal[1].charAt(i))) >= 0 && Integer.parseInt(String.valueOf(octal[1].charAt(i))) <= 8) {
                    n = true;
                } else {
                    n = false;
                }
            }
        }
        return n;
    }

