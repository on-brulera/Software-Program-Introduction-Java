    private void btBinarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBinarioActionPerformed

        if (txtNumero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor digite un valor");
        } else {
            if (ValidarOctal()) {
                String numero = txtNumero.getText();
                octal = new Octal(numero);

                txtProceso.setText(octal.OctalABinario());
            } else {
                JOptionPane.showMessageDialog(null, "Por favor digite un valor Octal");
            }
        }
    }//GEN-LAST:event_btBinarioActionPerformed

